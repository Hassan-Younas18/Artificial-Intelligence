from queue import PriorityQueue
from copy import deepcopy

class PuzzleState:
    """Represents a state of the 8-puzzle"""

    def __init__(self, board, parent=None, move=None, depth=0):
        self.board = board
        self.parent = parent
        self.move = move
        self.depth = depth
        self.h_value = 0  # Heuristic value

    def __lt__(self, other):
        """Compare states based on heuristic value for priority queue"""
        return self.h_value < other.h_value

    def __eq__(self, other):
        """Check if two states are equal"""
        return self.board == other.board

    def __hash__(self):
        """Hash function for state comparison"""
        return hash(str(self.board))

    def get_blank_position(self):
        """Find the position of the blank tile (0)"""
        for i in range(3):
            for j in range(3):
                if self.board[i][j] == 0:
                    return i, j
        return None

    def is_goal(self, goal_state):
        """Check if current state is the goal state"""
        return self.board == goal_state.board

    def get_successors(self):
        """Generate all possible successor states"""
        successors = []
        blank_row, blank_col = self.get_blank_position()

        # Possible moves: Up, Down, Left, Right
        moves = [
            (-1, 0, "Up"),
            (1, 0, "Down"),
            (0, -1, "Left"),
            (0, 1, "Right")
        ]

        for dr, dc, move_name in moves:
            new_row = blank_row + dr
            new_col = blank_col + dc

            # Check if the move is valid
            if 0 <= new_row < 3 and 0 <= new_col < 3:
                # Create new board configuration
                new_board = deepcopy(self.board)
                # Swap blank with adjacent tile
                new_board[blank_row][blank_col] = new_board[new_row][new_col]
                new_board[new_row][new_col] = 0

                # Create new state
                successor = PuzzleState(new_board, self, move_name, self.depth + 1)
                successors.append(successor)

        return successors

    def display(self):
        """Display the puzzle board"""
        for row in self.board:
            print(" ".join(str(tile) if tile != 0 else "*" for tile in row))
        print()

def manhattan_distance(state, goal_state):
    """
    Calculate Manhattan distance heuristic
    Sum of distances of each tile from its goal position
    """
    distance = 0
    for i in range(3):
        for j in range(3):
            if state.board[i][j] != 0:  # Skip blank tile
                value = state.board[i][j]
                # Find goal position of this value
                for goal_i in range(3):
                    for goal_j in range(3):
                        if goal_state.board[goal_i][goal_j] == value:
                            distance += abs(i - goal_i) + abs(j - goal_j)
                            break
    return distance

def misplaced_tiles(state, goal_state):
    """
    Calculate misplaced tiles heuristic
    Count of tiles not in their goal position
    """
    count = 0
    for i in range(3):
        for j in range(3):
            if state.board[i][j] != 0 and state.board[i][j] != goal_state.board[i][j]:
                count += 1
    return count

def greedy_best_first_search(initial_state, goal_state, heuristic_func=manhattan_distance):
    """
    Greedy Best-First Search Algorithm

    Args:
        initial_state: Starting state of the puzzle
        goal_state: Goal state of the puzzle
        heuristic_func: Heuristic function to use (default: manhattan_distance)

    Returns:
        Solution path and statistics
    """
    # Step 1: Place the starting node into the OPEN list
    open_list = PriorityQueue()
    initial_state.h_value = heuristic_func(initial_state, goal_state)
    open_list.put((initial_state.h_value, id(initial_state), initial_state))

    # CLOSED list to store expanded nodes
    closed_list = set()

    # Statistics
    nodes_expanded = 0
    max_depth = 0

    print("Starting Greedy Best-First Search...")
    print("Initial State:")
    initial_state.display()
    print(f"Initial heuristic value: {initial_state.h_value}\n")

    while True:
        # Step 2: If the OPEN list is empty, Stop and return failure
        if open_list.empty():
            print("Failure: Goal state not reachable!")
            return None, nodes_expanded, max_depth

        # Step 3: Remove the node n with the lowest h(n) value from OPEN list
        _, _, current_state = open_list.get()

        # Add to CLOSED list
        closed_list.add(current_state)
        nodes_expanded += 1
        max_depth = max(max_depth, current_state.depth)

        print(f"Expanding node {nodes_expanded} (depth={current_state.depth}, h={current_state.h_value}):")
        current_state.display()

        # Step 4 & 5: Expand the node and check if any successor is goal
        successors = current_state.get_successors()

        for successor in successors:
            # Check if successor is goal node
            if successor.is_goal(goal_state):
                print("Goal state found!")
                return successor, nodes_expanded, max_depth

            # Step 6: Calculate heuristic and add to OPEN list if not visited
            if successor not in closed_list:
                # Check if already in open list (simplified - not checking open list)
                successor.h_value = heuristic_func(successor, goal_state)
                open_list.put((successor.h_value, id(successor), successor))

    # Step 7: Return to Step 2 (loop continues)

def get_solution_path(goal_state):
    """Reconstruct the solution path from goal to initial state"""
    path = []
    current = goal_state

    while current is not None:
        path.append(current)
        current = current.parent

    path.reverse()
    return path

def print_solution(solution_path):
    """Print the solution path"""
    print("\n" + "="*50)
    print("SOLUTION PATH")
    print("="*50)

    for i, state in enumerate(solution_path):
        if state.move:
            print(f"Step {i}: Move {state.move}")
        else:
            print(f"Step {i}: Initial State")
        state.display()

    print(f"Total moves: {len(solution_path) - 1}")

