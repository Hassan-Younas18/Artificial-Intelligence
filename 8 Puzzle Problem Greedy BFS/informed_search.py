def a_star_search(initial_state, goal_state, heuristic_func=manhattan_distance):
    """
    A* Search Algorithm
    f(n) = g(n) + h(n)
    where g(n) is the cost from start to n
    and h(n) is the heuristic estimate from n to goal
    """
    open_list = PriorityQueue()

    # Calculate f(n) = g(n) + h(n)
    initial_state.h_value = heuristic_func(initial_state, goal_state)
    initial_state.g_value = 0  # Cost from start
    f_value = initial_state.g_value + initial_state.h_value

    open_list.put((f_value, id(initial_state), initial_state))

    closed_list = set()
    nodes_expanded = 0
    max_depth = 0

    print("Starting A* Search...")
    print("Initial State:")
    initial_state.display()
    print(f"Initial f(n) = g(n) + h(n) = {initial_state.g_value} + {initial_state.h_value} = {f_value}\n")

    while True:
        if open_list.empty():
            print("Failure: Goal state not reachable!")
            return None, nodes_expanded, max_depth

        _, _, current_state = open_list.get()

        if current_state in closed_list:
            continue

        closed_list.add(current_state)
        nodes_expanded += 1
        max_depth = max(max_depth, current_state.depth)

        f_val = current_state.g_value + current_state.h_value
        print(f"Expanding node {nodes_expanded} (depth={current_state.depth}, g={current_state.g_value}, h={current_state.h_value}, f={f_val}):")
        current_state.display()

        successors = current_state.get_successors()

        for successor in successors:
            if successor.is_goal(goal_state):
                print("Goal state found!")
                successor.g_value = current_state.g_value + 1
                successor.h_value = 0
                return successor, nodes_expanded, max_depth

            if successor not in closed_list:
                successor.g_value = current_state.g_value + 1  # Each move costs 1
                successor.h_value = heuristic_func(successor, goal_state)
                f_value = successor.g_value + successor.h_value
                open_list.put((f_value, id(successor), successor))


